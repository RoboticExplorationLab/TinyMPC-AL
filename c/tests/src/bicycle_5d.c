void tiny_Bicycle5dGetJacobians(Matrix* A, Matrix* B, const Matrix x,
                                const Matrix u) {
  tiny_Bicycle5dGetJacobianA_Raw(A->data, x.data, u.data);
  tiny_Bicycle5dGetJacobianB_Raw(B->data, x.data, u.data);
}