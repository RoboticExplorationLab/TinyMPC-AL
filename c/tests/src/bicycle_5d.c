      0.16666666666666666 *
      (0.1 * (0.1 * u[0] + x[3]) *
           cos(0.1 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) + x[2]) +
       0.1 * cos(x[2]) * x[3] +
       0.2 * (0.05 * u[0] + x[3]) *
           cos(0.05 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) + x[2]) +
       0.2 * (0.05 * u[0] + x[3]) * cos(0.05 * tan(x[4]) * x[3] + x[2]));
