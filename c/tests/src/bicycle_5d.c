  A[4] = 0;
  A[5] = 0;
  A[6] = 1;
  A[7] = 0;
  A[8] = 0;
  A[9] = 0;
  A[10] =
      0.16666666666666666 *
      (-0.2 * (0.05 * u[0] + x[3]) *
           sin(0.05 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) + x[2]) +
       -0.2 * (0.05 * u[0] + x[3]) * sin(0.05 * tan(x[4]) * x[3] + x[2]) +
       -0.1 * (0.1 * u[0] + x[3]) *
           sin(0.1 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) + x[2]) +
       -0.1 * sin(x[2]) * x[3]);
