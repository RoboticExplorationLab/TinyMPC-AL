//========================================
// Codes generated from julia/bicycle_tvlqr
// Discrete dynamics of bicycle model with predefined model params
//========================================
void tiny_Bicycle5dNonlinearDynamics_Raw(sfloat* xn, const sfloat* x,
                                         const sfloat* u) {
  xn[0] =
      0.16666666666666666 *
          (0.1 * (0.1 * u[0] + x[3]) *
               cos(0.1 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) +
                   x[2]) +
           0.1 * cos(x[2]) * x[3] +
           0.2 * (0.05 * u[0] + x[3]) *
               cos(0.05 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) +
                   x[2]) +
           0.2 * (0.05 * u[0] + x[3]) * cos(0.05 * tan(x[4]) * x[3] + x[2])) +
      x[0];
  xn[1] =
      0.16666666666666666 *
          (0.2 * (0.05 * u[0] + x[3]) *
               sin(0.05 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) +
                   x[2]) +
           0.2 * (0.05 * u[0] + x[3]) * sin(0.05 * tan(x[4]) * x[3] + x[2]) +
           0.1 * (0.1 * u[0] + x[3]) *
               sin(0.1 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) +
                   x[2]) +
           0.1 * sin(x[2]) * x[3]) +
      x[1];
  xn[2] = 0.16666666666666666 *
              (0.1 * (0.1 * u[0] + x[3]) * tan(0.1 * u[1] + x[4]) +
               0.4 * (0.05 * u[0] + x[3]) * tan(0.05 * u[1] + x[4]) +
               0.1 * tan(x[4]) * x[3]) +
          x[2];
  xn[3] = 0.1 * u[0] + x[3];
  xn[4] = 0.1 * u[1] + x[4];
}

void tiny_Bicycle5dNonlinearDynamics(Matrix* xn, const Matrix x,
                                     const Matrix u) {
  tiny_Bicycle5dNonlinearDynamics_Raw(xn->data, x.data, u.data);
}

